/* 문제 2 : 숫자를 매개변수로 받아 그 숫자의 제곱을 반환하는 함수 'squareNumber'를 작성하세요. */

function squareNumber(num) {
    // 여기에 코드를 작성하세요
    return num ** 2;
}

const result1 = squareNumber(4);
const result2 = squareNumber(-3);
const result3 = squareNumber(0);

console.log(result1 + result2 + result3); // 25
